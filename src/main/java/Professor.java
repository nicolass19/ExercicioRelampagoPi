public class Professor {

    private String escolaridade;
    private String cidadeNatural;
    private String tipoEnsino;

    public String getEscolaridade() {
        return escolaridade;
    }

    public Professor setEscolaridade(String escolaridade) {
        this.escolaridade = escolaridade;
        return this;
    }

    public String getCidadeNatural() {
        return cidadeNatural;
    }

    public Professor setCidadeNatural(String cidadeNatural) {
        this.cidadeNatural = cidadeNatural;
        return this;
    }

    public String getTipoEnsino() {
        return tipoEnsino;
    }

    public Professor setTipoEnsino(String tipoEnsino) {
        this.tipoEnsino = tipoEnsino;
        return this;
    }
}
